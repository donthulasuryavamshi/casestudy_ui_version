package com.casestudy.entities;

public class Venue {
	private String venue_id;
	private String venue_name;
	private int venue_capacity;
	private String venue_location;
	public String getVenue_id() {
		return venue_id;
	}
	public void setVenue_id(String venue_id) {
		this.venue_id = venue_id;
	}
	public String getVenue_name() {
		return venue_name;
	}
	public void setVenue_name(String venue_name) {
		this.venue_name = venue_name;
	}
	public int getVenue_capacity() {
		return venue_capacity;
	}
	public void setVenue_capacity(int venue_capacity) {
		this.venue_capacity = venue_capacity;
	}
	public String getVenue_location() {
		return venue_location;
	}
	public void setVenue_location(String venue_location) {
		this.venue_location = venue_location;
	}
	
}
